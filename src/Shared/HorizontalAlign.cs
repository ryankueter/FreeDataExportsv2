namespace FreeDataExportsv2;

/// <summary>
/// Constants for the CellOptions.HorizontalAlign property.
/// </summary>
public static class HorizontalAlign
{
    /// <summary>Align content to the left edge of the cell.</summary>
    public const string Left    = "left";

    /// <summary>Center content horizontally within the cell.</summary>
    public const string Center  = "center";

    /// <summary>Align content to the right edge of the cell.</summary>
    public const string Right   = "right";

    /// <summary>Repeat content to fill the cell width.</summary>
    public const string Fill    = "fill";

    /// <summary>Justify content across the full cell width.</summary>
    public const string Justify = "justify";

    /// <summary>Use the application default alignment for the cell's data type.</summary>
    public const string General = "general";
}
