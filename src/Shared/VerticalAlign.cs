namespace FreeDataExportsv2;

/// <summary>
/// Constants for the CellOptions.VerticalAlign property.
/// </summary>
public static class VerticalAlign
{
    /// <summary>Align content to the top edge of the cell.</summary>
    public const string Top     = "top";

    /// <summary>Center content vertically within the cell.</summary>
    public const string Center  = "center";

    /// <summary>Align content to the bottom edge of the cell.</summary>
    public const string Bottom  = "bottom";

    /// <summary>Distribute content evenly between the top and bottom of the cell.</summary>
    public const string Justify = "justify";
}
