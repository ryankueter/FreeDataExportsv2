namespace FreeDataExportsv2;

/// <summary>
/// Constants for the CellOptions border style properties
/// (BorderLeftStyle, BorderRightStyle, BorderTopStyle, BorderBottomStyle).
/// </summary>
public static class BorderStyle
{
    /// <summary>Thin solid line. The most common border style.</summary>
    public const string Thin             = "thin";

    /// <summary>Medium-weight solid line.</summary>
    public const string Medium           = "medium";

    /// <summary>Thick solid line.</summary>
    public const string Thick            = "thick";

    /// <summary>Hairline — the thinnest possible border.</summary>
    public const string Hair             = "hair";

    /// <summary>Double solid line.</summary>
    public const string Double           = "double";

    /// <summary>Thin dashed line.</summary>
    public const string Dashed           = "dashed";

    /// <summary>Medium-weight dashed line.</summary>
    public const string MediumDashed     = "mediumDashed";

    /// <summary>Thin dotted line.</summary>
    public const string Dotted           = "dotted";

    /// <summary>Thin dash-dot pattern.</summary>
    public const string DashDot          = "dashDot";

    /// <summary>Medium-weight dash-dot pattern.</summary>
    public const string MediumDashDot    = "mediumDashDot";

    /// <summary>Thin dash-dot-dot pattern.</summary>
    public const string DashDotDot       = "dashDotDot";

    /// <summary>Medium-weight dash-dot-dot pattern.</summary>
    public const string MediumDashDotDot = "mediumDashDotDot";

    /// <summary>Slanted dash-dot pattern.</summary>
    public const string SlantDashDot     = "slantDashDot";
}
